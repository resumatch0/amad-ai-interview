def record_audio(duration=120):
    """
    Records audio from the browser microphone.
    A 'Stop Recording' button appears in the output — click it when you are done speaking.
    - duration: maximum recording time in seconds as a safety fallback (default 120s)
    """
    print(f"\nLISTENING... Click the 'Stop Recording' button when you are done.")

    # The JS definition and the call are combined into one eval_js call
    # so that the record function is available in the same context
    RECORD_JS = f"""
    new Promise(async resolve => {{
      const stream = await navigator.mediaDevices.getUserMedia({{ audio: true }});
      const mediaRecorder = new MediaRecorder(stream);
      let chunks = [];

      mediaRecorder.ondataavailable = e => chunks.push(e.data);
      mediaRecorder.start();

      const btn = document.createElement('button');
      btn.textContent = 'Stop Recording';
      btn.style.cssText = `
        padding: 10px 24px;
        background-color: #c0392b;
        color: white;
        border: none;
        border-radius: 6px;
        font-size: 15px;
        cursor: pointer;
        margin-top: 8px;
      `;
      document.body.appendChild(btn);

      const timeout = setTimeout(() => mediaRecorder.stop(), {duration * 1000});

      btn.onclick = () => {{
        clearTimeout(timeout);
        mediaRecorder.stop();
        btn.disabled = true;
        btn.textContent = 'Recording stopped.';
      }};

      mediaRecorder.onstop = async () => {{
        const blob = new Blob(chunks);
        const reader = new FileReader();
        reader.readAsDataURL(blob);
        reader.onloadend = () => resolve(reader.result);
        stream.getTracks().forEach(track => track.stop());
        setTimeout(() => {{ if (btn.parentNode) btn.remove(); }}, 1500);
      }};
    }})
    """

    s = output.eval_js(RECORD_JS)
    b = b64decode(s.split(',')[1])
    filename = 'input_audio.wav'
    with open(filename, 'wb') as f:
        f.write(b)
    print("Audio captured.")
    return filename


